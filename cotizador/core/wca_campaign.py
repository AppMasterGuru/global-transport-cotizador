# ── Sender address (env-gated, no hardcoded default) ─────────────────────────

def _get_sender_email() -> str:
    """
    Return WCA_SENDER_EMAIL from environment.
    Raises ValueError with an actionable message if the variable is absent
    or blank — this forces Renato to confirm the sending address before
    any ZIP is produced.
    """
    email = os.environ.get("WCA_SENDER_EMAIL", "").strip()
    if not email:
        raise ValueError(
            "WCA_SENDER_EMAIL not set — confirm sending address with Renato before generating."
        )
    return email


