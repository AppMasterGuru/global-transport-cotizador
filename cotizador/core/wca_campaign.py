"""
WCA Pilot Campaign Generator.

Generates a ZIP archive containing:
  capability_deck.txt
  emails/email_001_CompanyName.txt … email_0N_CompanyName.txt
  tracking_[country]_[vertical].csv

Outputs bilingual ES+EN capability deck; outreach emails
in the requested language (es/en/de).

WCA network safety rule (from MEMORY.md):
  Use WCA's own tools (Broadcast, member messaging) where possible.
  Mass external blasting risks getting GT flagged or sanctioned.
  The emails generated here are personalised for careful, targeted use —
  one fully-filled email per named agent, not bulk templates.

Sender address:
  Read from WCA_SENDER_EMAIL env var — NO hardcoded default.
  Raises ValueError if not set, so Renato must confirm the address
  before any ZIP is produced.

Pilot agent lists:
  _PILOT_AGENTS keyed by (country_lower, vertical_lower).
  Italy + Textiles (25 agents) is the first live pilot list.
  When a list exists for the requested country+vertical it is used
  automatically by generate_wca_campaign(); otherwise the function
  falls back to n_agents anonymous placeholders.
"""

