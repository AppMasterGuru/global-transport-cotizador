def get_pilot_agents(country: str, vertical: str) -> list[dict]:
    """
    Return the hardcoded pilot agent list for this country+vertical, or [] if none.

    Keys are normalised to lowercase for lookup. The Italy+Textiles list is
    accessible via get_pilot_agents("Italia", "textiles") or any capitalisation.
    """
    key = (country.lower().strip(), vertical.lower().strip())
    return _PILOT_AGENTS.get(key, [])


# ── Helpers ──────────────────────────────────────────────────────────────────

_SAFE_NAME_RE = re.compile(r"[^a-z0-9_]")


def _safe(s: str) -> str:
    return _SAFE_NAME_RE.sub("_", s.lower().strip())


def _company_slug(company: str) -> str:
    """Short safe slug for filenames — max 30 chars, no trailing underscores."""
    return _safe(company)[:30].rstrip("_")


# ── Main generator ───────────────────────────────────────────────────────────

def generate_wca_campaign(
    country: str,
    vertical: str,
    n_agents: int = 15,
    language: str = "es",
    sender_name: str = "Renato Alvarez",
    agents: list[dict] | None = None,
) -> bytes:
    """
    Return ZIP bytes containing:
      capability_deck.txt
      emails/email_001_CompanyName.txt … (personalised) or email_001.txt … (fallback)
      tracking_[country]_[vertical].csv

    Args:
        country:     Target country for the campaign.
        vertical:    Cargo vertical key (see _VERTICAL_ES).
        n_agents:    Number of anonymous agents when no agent list is provided
                     (fallback mode). Clamped to 10–30.
        language:    Email language: "es" | "en" | "de".
        sender_name: GT representative name for signatures.
        agents:      Optional list of dicts with keys "name", "company", "email".
                     When provided, generates one fully-personalised email per
                     agent with [AGENT_NAME] and [AGENT_COMPANY] replaced.
                     When None, falls back to n_agents anonymous templates.

    Raises:
        ValueError: If WCA_SENDER_EMAIL env var is not set.
    """
    sender_email = _get_sender_email()   # fail fast — no ZIP without confirmed address

    n_agents = max(10, min(30, int(n_agents)))
    lang     = language.lower()
    if lang not in _EMAIL_TMPLS:
        lang = "es"

    vertical_label = vertical.title()
    vertical_es    = _VERTICAL_ES.get(vertical.lower(), vertical)
    vertical_en    = _VERTICAL_EN.get(vertical.lower(), vertical)

    params = dict(
        country=country,
        vertical_label=vertical_label,
        vertical_es=vertical_es,
        vertical_en=vertical_en,
        sender_name=sender_name,
        sender_email=sender_email,
    )

    # Capability deck — one copy, same for all agents
    deck_text = _DECK_TMPL.safe_substitute(**params).encode("utf-8")

    email_tmpl = _EMAIL_TMPLS[lang]
    base_text  = email_tmpl.safe_substitute(**params)  # $vars resolved; [BRACKETS] remain

    # Tracking CSV
    csv_buf = io.StringIO()
    writer  = csv.writer(csv_buf)
    writer.writerow(TRACKING_COLUMNS)

    # Build ZIP in memory
    zip_buf = io.BytesIO()
    with zipfile.ZipFile(zip_buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("capability_deck.txt", deck_text)

        if agents:
            # ── Personalised mode ────────────────────────────────────────────
            for i, agent in enumerate(agents, 1):
                name    = agent.get("name",    f"[Agente {i:02d}]")
                company = agent.get("company", "[Empresa]")
                email   = agent.get("email",   "")

                personalised = (
                    base_text
                    .replace("[AGENT_NAME]",    name)
                    .replace("[AGENT_COMPANY]", company)
                )

                slug  = _company_slug(company)
                fname = f"emails/email_{i:03d}_{slug}.txt"
                zf.writestr(fname, personalised.encode("utf-8"))

                writer.writerow([name, company, email, "", "", ""])

        else:
            # ── Fallback mode: anonymous templates ───────────────────────────
            for i in range(1, n_agents + 1):
                fname = f"emails/email_{i:03d}.txt"
                zf.writestr(fname, base_text.encode("utf-8"))

                writer.writerow([
                    f"[Agente {i:02d}]", "[Empresa]", "[email@agente.com]", "", "", "",
                ])

        csv_name = f"tracking_{_safe(country)}_{_safe(vertical)}.csv"
        zf.writestr(csv_name, csv_buf.getvalue().encode("utf-8"))

    zip_buf.seek(0)
    return zip_buf.read()
